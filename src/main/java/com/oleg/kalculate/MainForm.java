    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        double number2;
        if (line1.equals(""))
            number2 = number1;
        else
            number2 = Double.parseDouble(line1);        
        String result = jLabel3.getText();
        jLabel3.setText(result + " " + String.valueOf(number2) + " =");               
        if (znak == '+') {
            res = number1 + number2;
        }
        if (znak == '-') {
            res = number1 - number2;
        }
        if (znak == '*') {
            res = number1 * number2;
        }
        if (znak == '/') {
            res = number1 / number2;
        }        
        line1 = "";        
        jTextField1.setText(String.valueOf(res));       
    }//GEN-LAST:event_jButton17ActionPerformed

